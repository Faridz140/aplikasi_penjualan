    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String id = jfid.getText();
        String nama = jfnama.getText();
        String username = jfusername.getText();
        String password = jfpass.getText();
        String jenis_kelamin = "";
            if(jRadioButton1.isSelected()){
                jenis_kelamin="Laki-Laki";
            }
            else if(jRadioButton2.isSelected()){
                jenis_kelamin="Perempuan";
            }
        String alamat = jTextArea1.getText();
        String level = jComboBox1.getSelectedItem().toString();

        try{
            Statement statement = (Statement)koneksi.koneksiDb().createStatement();
            statement.executeUpdate("insert into member VALUES ('"+ id +"','"+ nama +"','"+ username +"','" + password +"','" + jenis_kelamin +"','" + alamat +"','" + level +"');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        }catch(Exception t){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan");
        }
        datatable();


        refresh();
       
    }//GEN-LAST:event_jButton2ActionPerformed

