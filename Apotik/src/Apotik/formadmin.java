    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(jButton3.getText() == "jButton3"){
           jfid.requestFocus();
        }else{
            String id = jfid.getText();
            String nama = jfnama.getText();
            String username = jfusername.getText();
            String password = jfpass.getText();
            String jenis_kelamin = "";
            if(jRadioButton1.isSelected()){
                jenis_kelamin="Laki-Laki";
            }
            else if(jRadioButton2.isSelected()){
                jenis_kelamin="Perempuan";
            }
            String alamat = jTextArea1.getText();
            String level = jComboBox1.getSelectedItem().toString();

            try{
                Statement statement = (Statement)koneksi.koneksiDb().createStatement(); //   
                statement.executeUpdate("UPDATE member SET id ='"+ id + "',"+"nama = '"+ nama +"',"+"username = '"+ username +"',"+" password = '"+ password +"',"+"jenis_kelamin = '"+ jenis_kelamin +"',"+" alamat = '"+ alamat +"',"+"level = '"+ level +"'" + "WHERE id ='"+id+"' OR nama='"+nama+"'");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            }catch(Exception t){
                JOptionPane.showMessageDialog(null, "Data Gagal Diubah");
            }
            datatable();
        }

        refresh();
    }//GEN-LAST:event_jButton3ActionPerformed

